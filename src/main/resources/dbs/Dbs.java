package dbs;

public class Dbs {

	private int dbsID;
	private String dbsTitle;
	private String userNeme;
	private String dbsDate;
	private String dbsContent;
	private String dbsAvailable;
	
	
	public int getDbsID() {
		return dbsID;
	}
	public void setDbsID(int dbsID) {
		this.dbsID = dbsID;
	}
	public String getDbsTitle() {
		return dbsTitle;
	}
	public void setDbsTitle(String dbsTitle) {
		this.dbsTitle = dbsTitle;
	}
	public String getUserNeme() {
		return userNeme;
	}
	public void setUserNeme(String userNeme) {
		this.userNeme = userNeme;
	}
	public String getDbsDate() {
		return dbsDate;
	}
	public void setDbsDate(String dbsDate) {
		this.dbsDate = dbsDate;
	}
	public String getDbsContent() {
		return dbsContent;
	}
	public void setDbsContent(String dbsContent) {
		this.dbsContent = dbsContent;
	}
	public String getDbsAvailable() {
		return dbsAvailable;
	}
	public void setDbsAvailable(String dbsAvailable) {
		this.dbsAvailable = dbsAvailable;
	}
}
	